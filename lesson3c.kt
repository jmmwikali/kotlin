fun main() {
    // WHILE LOOP
    // Create a program able to print from 0 to 10
    
    var count = 0

    while (count <= 10) {
        println("The number is: $count")
        count++
    }

    // Create a while loop printing from 50 to 35 with steps of 2
    var number = 50
    while ( number >=35 ) {
        println(number)
        number -= 2
    }
}